def get_total_cost_of_meal():
    # original meal price
    meal_cost = float(raw_input("enter the meal cost:"))
    # tip percentage
    tip_percent = int(raw_input("enter the tip percent:"))
    # tax percentage
    tax_percent = int(raw_input("enter the tax percent:"))

    # Write your calculation code here
    tip = meal_cost * tip_percent / 100
    tax = meal_cost * tax_percent / 100

    # cast the result of the rounding operation to an int and save it as
    # total_cost
    total_cost = int(round(tip + tax + meal_cost))

    return str(total_cost)





























































































































































































































































































































































































































































































































































































































































































































































# Print your result

print("The total meal cost is " + get_total_cost_of_meal() + " dollars.")
